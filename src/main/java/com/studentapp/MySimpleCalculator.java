package com.studentapp;

public class MySimpleCalculator {
	
	 /**
     * Add 2 numbers
     * @param num1
     * @param num2
     * @return
     */
    public double add2Num(double num1, double num2){

        return num1 + num2;
    }

    /**
     * Sub 2 numbers
     * @param num1
     * @param num2
     * @return
     */
    public double sub2Num(double num1, double num2){

        return num1 - num2;
    }

    /**
     * mul 2 numbers
     * @param num1
     * @param num2
     * @return
     */
    public double mul2Num(double num1, double num2){

        return num1 * num2;
    }

    /**
     * del 2 numbers
     * @param num1
     * @param num2
     * @return
     */
    public double div2Num(double num1, double num2){

        return num1 / num2;
    }


}
